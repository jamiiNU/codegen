std::string CPartNum::GetPartNumber(unsigned int uID, unsigned int uCoreType, unsigned int *puProjectCode) const
{
	int i;

	if (uCoreType == 0)
	{
		for (i = 0; i < _countof(g_PartNumIDs); ++i)
		{
			if (g_PartNumIDs[i].uID == uID)
			{
				if (puProjectCode != NULL)
					*puProjectCode =  g_PartNumIDs[i].uProjectCode;

				return g_PartNumIDs[i].szPartNumber;
			}
		}
	}
	else
	{
		for (i = 0; i < _countof(g_PartNumIDs_51); ++i)
		{
			if (g_PartNumIDs_51[i].uID == uID)
			{
				if (puProjectCode != NULL)
					*puProjectCode =  g_PartNumIDs_51[i].uProjectCode;

				return g_PartNumIDs_51[i].szPartNumber;
			}
		}
	}

	if (puProjectCode != NULL)
		*puProjectCode = 0;

	char szBuf[64];
	_snprintf_s(szBuf, sizeof(szBuf), _TRUNCATE, "[%08x]", uID);
	return szBuf;
}

std::string CPartNum::GetPartNumber(unsigned int auUCID[], unsigned int uDefaultID) const
{
	char szBuf[64] = {0};

	if((auUCID[0] != 0xFFFFFFFF)
	|| (auUCID[1] != 0xFFFFFFFF)
	|| (auUCID[2] != 0xFFFFFFFF)
	|| (auUCID[3] != 0xFFFFFFFF))
	{
		if(((auUCID[3] >> 28) & 0x3) == 2) //Show normal part no.
		{
		}
		else if(((auUCID[3] >> 28) & 0x3) == 0) //Show PDID
		{
			_snprintf(szBuf, sizeof(szBuf), "[%08X]", uDefaultID);
		}
		else //Show special part no.
		{
			unsigned int uBlank = 0;
			for(unsigned int i = 0; i < 3; i++) //auUCID[0] ~ auUCID[2]
			{
				for(unsigned int j = 0; j < 4; j++) //each byte of auUCID[n] 
				{
					if((auUCID[2 - i] >> ((3 - j) * 8) & 0xFF) != 0xFF)
						szBuf[i * 4 + j] = auUCID[2 - i] >> ((3 - j) * 8) & 0xFF;
					else
						uBlank++;
				}
			}
			for(unsigned int i = 0; i < (12 - uBlank); i++) //auUCID[0] ~ auUCID[2] have 12 bytes
				szBuf[i] = szBuf[i + uBlank]; //delete first # of 0xff (uBlank)
			szBuf[12 - uBlank] = '\0';
		}
	}

	return szBuf;
}

